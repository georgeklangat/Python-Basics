# Arithmetic operators

num1 = 54
num2 = 34

print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
print(num1 / num2)
print(num1 % num2)


# comparison operators
print(num1 < num2)
print(num1 > num2)
print(num1 <= num2)
print(num1 >= num2)
print(num1 == num2) # EQUAL TO
print(num1 != num2) # NOT EQUAL TO

# Assignment Operators
w = 7
print(w)

w += 3
print(w)

b = 10
b *= 2
print(b)

#Logic Operators - and , or , not
print(56 < 100 and 45 < 23)
print(56 < 100 or 45 < 23)
print(not(56 < 100 or 45 < 23))