# Data Structures - Multiple values stored in a single variable

cars = ["toyota","Nissan","BMW"] # LIST-Ordered and changeable
fruits = ["banana","apple","orange"] # TUPLE- Ordered and not changeable
countries = {"Kenya","Uganda","Tanzania","Ethiopia"} # SET-unordered and not changeable
student = {
    "firstname": "jane",
    "age":25,
    "course":"web Development",
    "gender":"female",
} # Dictionary -key-value pair

print(cars)
print(fruits)
print(countries)
# print(student)
print(student["gender"])
print(type(student))